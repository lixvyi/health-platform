# 特殊模式：需要更精细匹配
# 肿瘤名称特殊处理（带"癌"、"瘤"字样的）
TUMOR_PATTERN = re.compile(
    r'(?:晚期|转移性|局部晚期|早期)?\s*(乳腺|肺|肝|胃|结直肠|直肠|结肠|前列腺|卵巢|宫颈|胰腺|食管|肾|膀胱|甲状腺)(?:癌|瘤)')

# 需要排除的无效内容
EXCLUDE_PATTERNS = [
    r'暂无数据',
    r'尚不明确',
    r'不适用',
    r'用法和',
    r'用途和',
]


def is_valid_text(text):
    """检查是否为有效的适应症文本"""
    for pat in EXCLUDE_PATTERNS:
        if re.search(pat, text):
            return False
    return True


def extract_symptoms(indications_text):
    """从适应症文本中提取所有症状/疾病"""
    if not indications_text:
        return []

    found = set()

    # 匹配已知疾病模式
    for pattern, symptom_name, category in DISEASE_PATTERNS:
        if re.search(pattern, indications_text):
            found.add((symptom_name, category))

    # 特殊匹配：肿瘤
    for m in TUMOR_PATTERN.finditer(indications_text):
        organ = m.group(1)
        full_name = f"{organ}癌"
        category = '肿瘤'
        found.add((full_name, category))

    return list(found)


